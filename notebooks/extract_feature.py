def extract_image_features(model, pixel_values):
    with torch.no_grad():
        image_features = model.extract_feature(pixel_values)
    return image_features

def extract_text_features(model, tokenizer, text):
    inputs = tokenizer(text, return_tensors='pt')
    input_ids = inputs.input_ids.to(model.device)
    with torch.no_grad():
        text_features = model.language_model.get_input_embeddings()(input_ids)
    print("Text Features:")
    print(text)
    print(text_features.shape)
    return text_features

